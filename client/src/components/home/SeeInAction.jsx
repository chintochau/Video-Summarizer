const SeeInAction = () => {
  const videoRef = useRef(null);

  // 點擊轉錄時跳轉視頻
  const handleTimestampClick = (time) => {
    let [hours, minutes, seconds] = time.split(":");
    let secondsTotal;

    if (time.split(":").length === 2) {
      secondsTotal = parseInt(hours) * 60 + parseFloat(minutes);
    } else if (time.split(":").length === 3) {
      secondsTotal =
        parseInt(hours) * 3600 + parseInt(minutes) * 60 + parseFloat(seconds);
    }

    if (videoRef && videoRef.current) {
      videoRef.current.currentTime = secondsTotal;
      if (videoRef.current.internalPlayer) {
        videoRef.current.internalPlayer.seekTo(secondsTotal);
      }
    }
  };

  const linkOverride = {
    a: {
      component: ({ children, href, ...props }) => {
        if (href.startsWith("#timestamp")) {
          const timestamp = children[0].split(" - ")[0];
          return (
            <a
              className={"text-blue-500 cursor-pointer"}
              onClick={() => handleTimestampClick(timestamp)}
            >
              {children}
            </a>
          );
        }
        return (
          <a href={href} {...props}>
            {children}
          </a>
        );
      },
    },
  };

  return (
    <div className="bg-gray-50">
      <h2 className="text-4xl font-bold text-center text-indigo-800 pt-12">
        See in Action
      </h2>
      <Tabs className="w-full text-center" defaultValue="video-0">
        <TabsList>
          {demoVideos.map((video, index) => (
            <TabsTrigger key={index} value={`video-${index}`}>
              {video.title}
            </TabsTrigger>
          ))}
        </TabsList>
        {demoVideos.map((video, index) => (
          <TabsContent key={index} value={`video-${index}`}>
            <Card className="border-indigo-50 shadow-md container px-0 lg:px-2">
              <CardTitle>
                <CardHeader>
                  {video.videoTitle}
                  <CardDescription className="mt-4">
                    {video.description}
                  </CardDescription>
                </CardHeader>
              </CardTitle>
              <CardContent className="flex flex-col md:flex-row gap-2 lg:gap-8">
                <div className="w-full md:w-2/5  lg:w-5/12 flex flex-col gap-4">
                  <YTVideoField
                    youtubeId={video.youtubeId}
                    homeMode
                    videoRef={videoRef}
                  />
                  <Link to="summarizer" className="hidden md:block">
                    <Button
                      className="w-full"
                      onClick={() => {
                        window.scrollTo(0, 0);
                      }}
                    >
                      Try your own video now
                    </Button>
                  </Link>
                </div>
                <ScrollArea className="w-full md:w-3/5 lg:w-7/12 h-80 md:h-[40vw] text-left overflow-auto border-l-indigo-50 border-l px-2 lg:px-8 ">
                  <Markdown
                    className="prose max-w-full"
                    options={{ overrides: linkOverride }}
                  >
                    {transformTimestampRangeFromArticleToSingleLink(
                      video.summary
                    )}
                  </Markdown>
                </ScrollArea>

                <Link to="summarizer" className="md:hidden pt-4">
                  <Button
                    className="w-full"
                    onClick={() => {
                      window.scrollTo(0, 0);
                    }}
                  >
                    Try your own video now
                  </Button>
                </Link>
              </CardContent>
            </Card>
            <div className="flex flex-col md:flex-row w-full justify-between items-center bg-gray-50 p-2 rounded-md"></div>
          </TabsContent>
        ))}
      </Tabs>
    </div>
  );
};

export default SeeInAction;

